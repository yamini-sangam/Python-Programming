# Given a list of names, print all names starting with the letter 'A'

# Sample list of names
names = ["Alice", "Bob", "Charlie", "David", "Anna", "Alex", "Ava"]

# Iterate through the list and print names starting with 'A'
print("Names starting with 'A':")
for name in names:
    if name[0] == 'A':
        print(name)
